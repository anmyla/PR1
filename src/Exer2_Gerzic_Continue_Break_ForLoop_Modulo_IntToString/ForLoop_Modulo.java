package Exer2_Gerzic_Continue_Break_ForLoop_Modulo_IntToString;

public class ForLoop_Modulo {
    public static void main(String[] args) {

        int num = 30;
        for (int i = 1; i <= num; i++) {
            if (i % 2 == 0) {
                System.out.println(i);
            }
        }
    }
}